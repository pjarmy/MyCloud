# 簇所表现的特征是：簇内样本相似度高（方差小），而簇间的相似度低（方差大）。关于聚类算法有
# 很多，如 K均值聚类、密度聚类、谱系聚类、最大期望聚类等。

#     本文我们介绍的是 K均值聚类，它是公认的十大挖掘算法之一，其优点是计算速度快、原理易于
